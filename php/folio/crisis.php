<h2 class="content-heading">Crisis Monitoring Module</h2>
<h3 class="small-heading">The purpose</h3>
<p>Crisis Monitoring Module allows a client to be alerted of any unusual increase of chatter in the social media which often indicates a  crisis. This crisis may be a result of a major issue affecting the client's customers that is uncovered by the public or the media. Early alert allows the client to have more time to prepare for an immediate communication response.</p>

<p>Click the picture below to see how SMRT shareholders could have used the tool designed by me to uncover a PR crisis that happened on 17 August 2012 as a result of a power fault in North East Line even before the news broke out.</p>

