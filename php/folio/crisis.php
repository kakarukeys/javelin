<h3 class="small-heading">The algorithm</h3>
<p>The module uses a special machine learning algorithm developed by me, called <strong>anomaly detection in time-series data</strong> to keep track of the normal profile of the total mentions and calculate the threshold curve above which an anomaly is detected. Other features of the module include</p>

<ul>
    <li>The charting function and the user interface</li>
    <li>The text data analysis and summary function</li>
    <li>The monitoring and automatic email alert script</li>
</ul>

