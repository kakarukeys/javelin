<h2 class="content-heading">BlitzType</h2>
<h3 class="small-heading">Dyslexia Assistive Technology</h3>
<p>Dyslexia is a difficulty in the acquisition of fluent reading and writing skills that is neurological in origin. In the digital age, dyslexic PC users face difficulties when typing and writing a piece of text down on a PC. BlitzType is designed for reducing key strokes needed in text input and therefore increasing the typing speed of dyslexic users and making them productive.</p>

<p>It does so by memorizing what the user has typed and trying to predict what the user is about to type with a clever algorithm. It offers choices of completed words in a small popup window. As the user presses a hotkey, the word or phrase is autocompleted for him.</p>
